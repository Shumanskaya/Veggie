function photoBlockUp(i, timeout) {
    let selector = '.section_spec_'
    setTimeout(function () {
        $(selector + 'photo' + i).addClass('up')
    }, timeout * i);
    setTimeout(function () {
        $(selector + 'block' + i).addClass('up')
    }, timeout * i + timeout * 6);
}


$(window).scroll(function () {
    let w = $('body').scrollTop()
    if (w >= 1100) {
        for (let i = 0; i <= 6; i++) {
            photoBlockUp(i, 150)
        }
        $('.backTotop').css('display', 'block')
    }
    else {
        $('.backTotop').css('display', 'none')  
    }
})

$('.backTotop').click(function() {
    $('body').animate({
        scrollTop: 0
    }, 1000)
})
