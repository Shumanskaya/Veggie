function addMenu(menu) {
    for (let i in menu) {
        let name = menu[i].name
        let price = menu[i].price
        let list = menu[i].list
        if (i < (menu.length / 2)) {
            addMenuFirstColumn(name, price, list)
        }
        else {
            addMenusecondColumn(name, price, list)
        }
    }
}

function emptyMenu() {
    $('.test1').empty('<div class="flexMenu"><div class="name"><p class="text-uppercase"></p></div><div class="dots"></div><div class="price"><p></p></div></div><div class="textMenu"></div>')
    $('.test2').empty('<div class="flexMenu"><div class="name"><p class="text-uppercase"></p></div><div class="dots"></div><div class="price"><p></p></div></div><div class="textMenu"></div>')
}

function addMenuFirstColumn(name, price, list) {
    $('.test1').append('<div class="flexMenu"><div class="name"><p class="text-uppercase">' + name + '</p></div><div class="dots"></div><div class="price"><p>' + price + '</p></div></div><div class="textMenu">' + list + '</div>')
}
function addMenusecondColumn(name, price, list) {
    $('.test2').append('<div class="flexMenu"><div class="name"><p class="text-uppercase">' + name + '</p></div><div class="dots"></div><div class="price"><p>' + price + '</p></div></div><div class="textMenu">' + list + '</div>')
}

function focusAndBlur(check) {
    console.log(check)
    $(check).focus(function () {
        $(this).css('border', '1px solid black')
    })
    $(check).blur(function () {
        $(this).css('border', 'none')
    })
}

[
    'starters',
    'mainDishes',
    'desserts',
    'drinks'
].forEach(function(i){
    let selector = $('#' + i);
    focusAndBlur(selector);
    selector.click(function(sel){
        emptyMenu();
        addMenu(eval(i))
    })
});

